rectangleLength = float(input("Enter the length of the rectangle: "))
rectangleWidth = float(input("Enter the width of the rectangle: "))
rectangleArea = rectangleWidth * rectangleLength
print(f"The rectangle area with length {rectangleArea} and width {rectangleWidth} is: {rectangleArea}")
import math
circleRadius = float(input("Enter the the radius of the circle: "))
circleArea = round(math.pi * pow(circleRadius, 2),2)
print(f"The area of a circle with radius {circleRadius} is {circleArea}")
triangleBase = float(input("Enter the base of the triangle: "))
